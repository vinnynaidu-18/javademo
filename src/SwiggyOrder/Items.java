package SwiggyOrder;

public class Items {
    public String Biriyani;
    public String Curry;
    public String Butternon;

    public Items(String biriyani, String curry, String Butternon) {
       this. Biriyani = biriyani;
        this.Curry = curry;
        this.Butternon = Butternon;
    }

    public String getBiriyani() {
        return Biriyani;
    }

    public String getCurry() {
        return Curry;
    }

    public String getButternon() {
        return Butternon;
    }
}